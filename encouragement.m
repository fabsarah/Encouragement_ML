    fprintf('You''re a fantastic scientist! \n');
elseif a==5
    fprintf('Who has infinite thumbs and thinks you''re great? Me! \n');
elseif a==6
    fprintf('If I could make hot beverages, I would make you a hot beverage! \n');
elseif a==7
    fprintf('Here is a Nobel prize! \n');
